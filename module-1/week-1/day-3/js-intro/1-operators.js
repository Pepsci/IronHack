// math
/*

+ : sum
- : substract
/ : divide
* : multiply
% : modulo

*/

console.log(1 + 1); // 2
console.log(1 - 1); // 0
console.log(10 / 2); // 5
console.log(2 * 2); // 4
console.log(10 % 3); // 1

// logical


// AND => &&
// OR  => || (option right + L)
// NOT => !

console.log(true && false);
console.log(true || false);
console.log(!true);
console.log(!false);
console.log(!true || 1 < 10);

console.log("------");
// comparison
// greater than 
console.log(100 > 10); // true
// less than
console.log(100 < 10); // false
// greater than equal
console.log(10 >= 10); // true
// less than equal
console.log(10 <= 10); // true
// loose equality
console.log(1 == "1"); // true
// strict equality
console.log(1 === "1"); // false
// loose non equality
console.log(1 != "1"); // false
// strict non equality
console.log(1 !== "1"); // true

// ternary
console.log("--------------");
console.log(10000 < 100 ? "yes" : "no")
